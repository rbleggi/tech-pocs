package com.rbleggi.agents

case class AgentTask(id: String, description: String, taskType: String)

case class Message(sender: String, recipient: String, content: String)

case class AgentResult(agent: String, task: String, result: String)

trait AgentStrategy:
  def process(task: AgentTask): AgentResult
  def receiveMessage(message: Message): String

class ResearcherAgent extends AgentStrategy:
  private val knowledgeBase = Map(
    "scala" -> "Functional and OO language for JVM, created by Martin Odersky",
    "kubernetes" -> "Open-source container orchestration system",
    "brasil" -> "Largest country in South America, capital Brasilia"
  )

  def process(task: AgentTask): AgentResult =
    val term = task.description.toLowerCase.split("\\s+").find(p => knowledgeBase.contains(p))

    term match
      case Some(t) =>
        AgentResult("researcher", task.id, s"Research about $t: ${knowledgeBase(t)}")
      case None =>
        AgentResult("researcher", task.id, "Term not found in knowledge base")

  def receiveMessage(message: Message): String =
    s"Researcher received: ${message.content}"

class WriterAgent extends AgentStrategy:
  def process(task: AgentTask): AgentResult =
    val text = task.description
    val words = text.split("\\s+").length
    val document = s"""
      |Document generated by Writer
      |============================
      |$text
      |
      |Statistics: $words words
      """.stripMargin

    AgentResult("writer", task.id, document)

  def receiveMessage(message: Message): String =
    s"Writer received: ${message.content}"

class ReviewerAgent extends AgentStrategy:
  def process(task: AgentTask): AgentResult =
    val text = task.description
    val errors = List(
      if text.length < 10 then Some("Text too short") else None,
      if !text.matches(".*[.!?]$") then Some("Missing final punctuation") else None,
      if text.toLowerCase == text then Some("No uppercase letters") else None
    ).flatten

    val result = if errors.isEmpty then
      "Review approved without errors"
    else
      s"Problems found: ${errors.mkString(", ")}"

    AgentResult("reviewer", task.id, result)

  def receiveMessage(message: Message): String =
    s"Reviewer received: ${message.content}"

class AgentSystem:
  private var agents: Map[String, AgentStrategy] = Map(
    "researcher" -> ResearcherAgent(),
    "writer" -> WriterAgent(),
    "reviewer" -> ReviewerAgent()
  )
  private var messages: List[Message] = List.empty

  def executeTask(agentName: String, task: AgentTask): Option[AgentResult] =
    agents.get(agentName).map(_.process(task))

  def sendMessage(message: Message): Unit =
    messages = messages :+ message
    agents.get(message.recipient).foreach(_.receiveMessage(message))

  def collaborate(task: AgentTask): List[AgentResult] =
    val research = agents("researcher").process(AgentTask(s"${task.id}-1", task.description, "research"))
    sendMessage(Message("researcher", "writer", research.result))

    val writing = agents("writer").process(AgentTask(s"${task.id}-2", research.result, "writing"))
    sendMessage(Message("writer", "reviewer", writing.result))

    val review = agents("reviewer").process(AgentTask(s"${task.id}-3", writing.result, "review"))

    List(research, writing, review)

  def getMessages: List[Message] = messages

@main def run(): Unit =
  println("AI Agent System")
